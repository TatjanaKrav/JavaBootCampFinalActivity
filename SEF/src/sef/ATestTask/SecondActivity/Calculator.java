package sef.ATestTask.SecondActivity;

public class Calculator {

		public int getSum(int a, int b) {
			return a + b;
		}
		
		public int getDifference(int a, int b){
			return a - b;
		}
		
		public double getProduct(double a1, double b1){
			return a1 * b1;
		}
		
		public double getQuotient(double a1, double b1){
			return a1 / b1;
		}


	}


